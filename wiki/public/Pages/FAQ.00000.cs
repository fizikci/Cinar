====Çınar kütüphanesinin kaynak kodlarını nasıl indirebilirim?====
* Öncelikle bilgisayarınıza Tortoise SVN Client programını kurun
* Cinar adında bir klasör oluşturun
* Bu klasörün üzerine sağ tıklayıp "SVN Checkout" menüsünü seçin
* SVN adresini girmeniz gereken yere [Download] sayfasındaki SVN adresimizi kopyalayın
* Tamam'a tıklayın. Bütün kaynak dosyaları bilgisayarınıza indirilecektir.