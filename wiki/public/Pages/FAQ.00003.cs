====Zaten bir SQL client kullanıyorum, Çınar'ı kullanmam için tek bir sebep söyleyin====
* Çok sebep söyleyebiliriz
* Birincisi Çınar programcılar tarafından programcılar için geliştirilmiştir.
* Biz yıllardır veritabanı uygulamaları geliştirmiş kişiler olarak sizin (ve bizim) ihtiyaçlarımızı çok iyi biliyoruz.
* Geleneksel SQL clientlar ile çalışırken ancak kodlamayla yapabileceğiniz işler için küçük programcıklar yazdığınızı biliyoruz.
* Aynı kodları defalarca yazdığınızı veya kopyala yapıştır ile benzer kodları çoğalttığınızı biliyoruz.
* Bir veritabanını incelerken SQL yazmaktan yorulduğunuzu da..
* Bütün bu ihtiyaçlarınızı karşılayacak bir ürün geliştirdik.
* Biz bunu büyük bir keyifle kullanıyoruz. Bize çok zaman kazandırıyor.
* Size de kazandırmasını istiyoruz.

====Hmm, peki o zaman Çınar kütüphanesinin kaynak kodlarını nasıl indirebilirim?====
* Öncelikle bilgisayarınıza [http://www.google.com/?q=Tortoise SVN Client|Tortoise SVN Client] programını kurun
* Cinar adında bir klasör oluşturun
* Bu klasörün üzerine sağ tıklayıp "SVN Checkout" menüsünü seçin
* SVN adresini girmeniz gereken yere [Download] sayfasındaki SVN adresimizi kopyalayın
* Tamam'a tıklayın. Kaplumbağa bütün kaynak dosyaları bilgisayarınıza indirecektir.