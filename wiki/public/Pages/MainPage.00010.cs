Çınar Teknoloji, yaşları 40'a dayanmış birkaç yazılımcının tecrübeleriyle zaman içinde geliştirdiği açık kaynaklı bir yazılım kütüphanesidir.

Bu kütüphane aşağıdaki teknolojileri sağlar:

<div class="cbox">
<div class="icon"><img alt="" src="/Images/cinar/cinar.database.png"></div>
<h3>[Cinar Database]</h3>
<h4>Table veya object oriented!</h4>
<p>MySQL, PostgreSQL, MS SQL farketmez. Hepsine aynı arabirimden erişin, metadata ile generic kodlar yazmanın tadını çıkarın.</p></div>
<div class="cbox">
<div class="icon"><img alt="" src="/Images/cinar/cinar.dbtools.jpg"></div>
<h3>[Cinar DBTools]</h3>
<h4>Sadece SQL client değil!</h4>
<p>Güçlü bir SQL client. Code Generator. Scheduler. Database transformer. Diagram maker. Scriptifiable.</p></div>
<div class="cbox">
<div class="icon"><img alt="" src="/Images/cinar/cinar.uicommands.jpg"></div>
<h3>[Cinar UICommands]</h3>
<h4>Butona çift tıklayıp kod yazmayı bırakın!</h4>
<p>Bir command pattern kütüphanesi. Uygulamanızı komutlar olarak tasarlayın. UI'de enable/disable uğraşmayın. Daha maintable.</p></div>
<div class="cbox">
<div class="icon"><img alt="" src="/Images/cinar/cinar.scripting.png"></div>
<h3>[Cinar Scripting]</h3>
<h4>Derlemeden gelişebilen uygulamalar yazın!</h4>
<p>Kullanıcınıza sağladığınız scripting desteği ile uygulamanızın sizin bile hayal edemeyeceğiniz işlevselliğe ulaşmasını sağlayın. Artı debugger ve code completion!</p></div>
<div class="cbox">
<div class="icon"><img alt="" src="/Images/cinar/cinar.extensions.png"></div>
<h3>[Cinar Extensions]</h3>
<h4>Aynı kodları çok yazdınız. Artık dinlenin!</h4>
<p>JSON, XML, Reflection, Drawing vb konularda yazılmış C# extensionlarını kullanın.</p></div>
<div class="cbox">
<div class="icon"><img alt="" src="/Images/cinar/cinar.famfamfam.png"></div>
<h3>[Cinar FamFamFam]</h3>
<h4>FamFamFam ikonları elinizin altında!</h4>
<p>Public resurce olarak derlenmiş bu kütüphaneyi kullanmak çok kolay.</p></div>

